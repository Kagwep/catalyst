"""Postgres backend for the store — a thin psycopg proxy that lets the existing
SQLite-flavoured SQL in ``store.py`` run unchanged against Supabase/Postgres.

The whole codebase talks to the database through the connection object returned
by ``store.open_store``: it calls ``conn.execute(sql, params)``, iterates the
result, wraps writes in ``with conn:`` and eventually ``conn.close()``. sqlite3
and psycopg both offer that surface, but with three incompatibilities this
module papers over so ``store.py`` needs no edits:

1. **Placeholders.** sqlite3 uses ``?`` (positional) and ``:name`` (named);
   psycopg uses ``%s`` and ``%(name)s``. ``_translate`` rewrites them.
2. **``with conn:`` semantics.** For sqlite3 it's *transaction only* (commit on
   success / rollback on error, connection stays open) and is used many times
   per connection. psycopg's own context manager *closes* the connection on
   exit, which would break the second ``with``. So the proxy implements the
   sqlite3 semantics explicitly.
3. **Row access.** ``store.py`` mixes mapping access (``row["col"]``,
   ``dict(row)``) with positional access (``fetchone()[0]``). ``_hybrid_row``
   returns rows that support both, like ``sqlite3.Row``.
"""

from __future__ import annotations

import os
import re

# psycopg is only needed when hosting on Postgres; keep it an optional import so
# the core package (local SQLite) has no hard dependency on it.
try:  # pragma: no cover - import guard
    import psycopg
except ModuleNotFoundError:  # pragma: no cover
    psycopg = None  # type: ignore[assignment]


# --- SQL dialect: Postgres mirror of store._SCHEMA -------------------------
# Same tables/columns/indexes as the SQLite schema, translated to Postgres:
#   INTEGER PRIMARY KEY AUTOINCREMENT -> BIGINT GENERATED ... AS IDENTITY
