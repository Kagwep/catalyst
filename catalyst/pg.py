# ``:name`` not preceded by another ``:`` (so Postgres ``::type`` casts, should
# any appear, are left alone), followed by an identifier.
_NAMED_PARAM = re.compile(r"(?<!:):([a-zA-Z_]\w*)")


def _translate(sql: str) -> str:
    """Rewrite sqlite3 placeholders to psycopg style.

    ``:name`` -> ``%(name)s`` and ``?`` -> ``%s``. The store's SQL never mixes
    the two styles in one statement and has no ``%`` literals or ``?`` inside
    string literals, so these two substitutions are safe.
    """
    sql = _NAMED_PARAM.sub(r"%(\1)s", sql)
    sql = sql.replace("?", "%s")
    return sql


def _hybrid_row(cursor):
    """psycopg row factory yielding rows usable by key *and* by position.

    ``dict(row)`` and ``row["col"]`` work (mapping), and ``row[0]`` works
    (position) — matching ``sqlite3.Row`` so ``store.py`` needs no changes.
    """
    cols = [d.name for d in cursor.description] if cursor.description else []

    def make(values):
        return _Row(cols, values)

    return make


class _Row(dict):
    """dict row that also supports integer indexing."""

    __slots__ = ("_values",)

    def __init__(self, cols, values):
        super().__init__(zip(cols, values))
        self._values = tuple(values)

    def __getitem__(self, key):
        if isinstance(key, int):
            return self._values[key]
        return super().__getitem__(key)


class PgConnection:
    """A sqlite3.Connection-shaped facade over a psycopg connection."""

    def __init__(self, conn):
        self._conn = conn

    # -- statement execution (mirrors sqlite3.Connection.execute) -----------
    def execute(self, sql: str, params=()):
        cur = self._conn.cursor()
        cur.execute(_translate(sql), params or None)
        return cur

    def executemany(self, sql: str, seq_of_params):
        cur = self._conn.cursor()
        cur.executemany(_translate(sql), list(seq_of_params))
        return cur

    def executescript(self, script: str):
        # psycopg's extended protocol allows one statement per execute, so run
        # the schema statement-by-statement. Safe here: no ';' inside literals.
        for stmt in script.split(";"):
            if stmt.strip():
                self._conn.execute(stmt)
        return self

    # -- transaction control (sqlite3 semantics: no close on __exit__) -------
    def __enter__(self):
        return self

    def __exit__(self, exc_type, exc, tb):
        if exc_type is not None:
            self._conn.rollback()
        else:
            self._conn.commit()
        return False  # don't suppress exceptions

    def commit(self):
        self._conn.commit()

    def rollback(self):
        self._conn.rollback()

    def close(self):
        self._conn.close()

    @property
    def raw(self):
        """The underlying psycopg connection (for pandas / COPY, if needed)."""
        return self._conn


def open_pg(url: str) -> PgConnection:
    """Connect to Postgres, ensure the schema exists, return the proxy.

    ``url`` is a standard ``postgresql://…`` DSN (the Supabase connection
    string). autocommit is off so ``with conn:`` blocks are real transactions,
    exactly like the SQLite path.
    """
    if psycopg is None:  # pragma: no cover
        raise RuntimeError(
            "DATABASE_URL is set but psycopg is not installed — "
            "install the hosting extra: pip install 'catalyst[pg]'"
        )
    # Optional out-of-band password: when DATABASE_PASSWORD is set it's passed to
    # libpq as a separate parameter, so the DSN needs no password and special
    # characters (@ : / # etc.) require NO percent-encoding. This is the robust
    # way to feed a Supabase DB password that contains symbols.
    extra: dict[str, object] = {}
    pw = os.environ.get("DATABASE_PASSWORD")
    if pw:
        extra["password"] = pw

    # prepare_threshold=None disables psycopg's automatic prepared statements,
    # which otherwise break under Supabase's transaction-mode pooler (pgbouncer
    # on port 6543). Harmless on the session pooler / direct connection, so this
    # makes any Supabase connection string work.
    conn = psycopg.connect(
        url, autocommit=False, row_factory=_hybrid_row, prepare_threshold=None, **extra
    )
    proxy = PgConnection(conn)
    proxy.executescript(_PG_SCHEMA)
    conn.commit()
    return proxy


def database_url() -> str | None:
    """The configured Postgres DSN, if any. Presence of this selects the backend."""
    return os.environ.get("DATABASE_URL") or None
